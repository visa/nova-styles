const fs = require('fs'); 
const path = require('path');

const { getFile } = require('../../../bin/fsio.cjs');

const SOURCE = '../styles/CHANGELOG.md';
const DEST = './src/data/docs/changelog.js';

const Process = (() => {
  const copyChangelog = () => {
    console.log("Copying changelog.\n");
    const changelog = getFile(path.resolve(SOURCE)); 
    const changelogFile = `// this file is autogenerated \n
const changelog = \`${changelog}\`; \n
export default changelog;`; 
    fs.writeFileSync(path.resolve(DEST), changelogFile); 
  };

  copyChangelog(); 
})();